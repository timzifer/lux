func init() {
	for ch := 'a'; ch <= 'z'; ch++ {
		glyphs[ch] = glyphs[unicode.ToUpper(ch)]
	}
	for _, ch := range strings.Split("äöüßÄÖÜ", "") {
		if ch == "" {
			continue
		}
		glyphs[[]rune(ch)[0]] = glyphs['?']
	}
}
